# 4. UNDERSTANDING INDEX AND VALUES
# ============================================================================

def demonstrate_index_and_values():
    """Inspect Series index and values with position and label access"""
    section_header("4. Understanding Index and Values")

    productivity_series = pd.Series(
        [88, 91, 84, 95],
        index=["Alice", "Bob", "Charlie", "Diana"],
    )

    print("Series with custom labels:")
    print(productivity_series)

    print("\nInspect components:")
    print(f"  Index: {list(productivity_series.index)}")
    print(f"  Values: {list(productivity_series.values)}")

    print("\nPositional vs label access:")
    print(f"  Positional access (iloc[1]): {productivity_series.iloc[1]}")
    print(f"  Label access (loc['Bob']): {productivity_series.loc['Bob']}")

    print("\nSimple label-aware behavior:")
    bonus_points = pd.Series([2, 1, 3, 2], index=["Alice", "Bob", "Charlie", "Diana"])
    updated_scores = productivity_series + bonus_points
    print(updated_scores)


