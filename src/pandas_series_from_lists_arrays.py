# ============================================================================
# 1. UNDERSTANDING PANDAS SERIES
# ============================================================================

def demonstrate_series_concept():
    """Demonstrate what a Pandas Series is"""
    section_header("1. Understanding Pandas Series")

    satisfaction_scores = pd.Series([4.2, 3.8, 4.5, 4.0])

    print("A Series is one-dimensional labeled data.")
    print("\nExample Series:")
    print(satisfaction_scores)

    print("\nConcept check:")
    print(f"  Type: {type(satisfaction_scores)}")
    print(f"  Default index: {list(satisfaction_scores.index)}")
    print(f"  Values: {list(satisfaction_scores.values)}")

    comparison_array = np.array([4.2, 3.8, 4.5, 4.0])
    print("\nSeries vs NumPy array:")
    print(f"  NumPy type: {type(comparison_array)}")
    print("  NumPy has values but no label index like Series.")


