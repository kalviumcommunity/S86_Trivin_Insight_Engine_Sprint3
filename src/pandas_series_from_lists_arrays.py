    print("Monthly sales Series:")
    print(monthly_sales)

    print("\nBasic operations:")
    print(f"  Mean: {monthly_sales.mean():.2f}")
    print(f"  Max: {monthly_sales.max()}")
    print(f"  Min: {monthly_sales.min()}")

    increased_sales = monthly_sales + 10
    print("\nAfter adding 10 to each month:")
    print(increased_sales)


