# 2. CREATING A SERIES FROM PYTHON LISTS
# ============================================================================

def demonstrate_series_from_list():
    """Create and inspect Series built from Python lists"""
    section_header("2. Creating a Series from Python Lists")

    departments = ["Engineering", "Sales", "Marketing", "HR"]
    department_series = pd.Series(departments)

    print("Series created from list:")
    print(department_series)

    print("\nAutomatic index generated by Pandas:")
    print(department_series.index)

    print("\nSimple access examples:")
    print(f"  Position 0: {department_series[0]}")
    print(f"  Position 2: {department_series[2]}")

    employee_counts = [24, 18, 11, 7]
    employee_count_series = pd.Series(employee_counts)

    print("\nNumeric list to Series:")
    print(employee_count_series)


# ============================================================================
# 3. CREATING A SERIES FROM NUMPY ARRAYS
# ============================================================================

def demonstrate_series_from_numpy_array():
    """Create and inspect Series built from NumPy arrays"""
    section_header("3. Creating a Series from NumPy Arrays")

    response_time_array = np.array([2.1, 1.8, 2.6, 1.9, 2.2], dtype=np.float64)
    response_time_series = pd.Series(response_time_array)

    print("NumPy array:")
    print(response_time_array)
    print(f"Array dtype: {response_time_array.dtype}")

    print("\nSeries from NumPy array:")
    print(response_time_series)
    print(f"Series dtype: {response_time_series.dtype}")

    print("\nData type is preserved when converting array -> Series.")

    print("\nWhy labels matter:")
    labeled_series = pd.Series(
        response_time_array,
        index=["Mon", "Tue", "Wed", "Thu", "Fri"],
    )
    print(labeled_series)


